while True:
    n=input("Press 'c' to close, 'o' to open and 'q' to quit:")
    
    if n.strip() == 'q':
        break
    elif n.strip() == 'o':
        print("Open\n")
    elif n.strip() == 'c':
        print ("Close\n")
    elif n.strip() == 'o1':
        print ("Open 1\n") 
           
